<template>
  <div>
    <div class="alert">
      <span v-if="close" class="closebtn">&times;</span>
      <slot>{{message}}</slot>
    </div>
    <!-- warning info success -->
  </div>
</template>

<script>
export default {
  props: {
    message: {
      type: [String, Function],
      default: ""
    },
    close: {
      type: Boolean,
      default: false
    }
  },
  mounted() {
    var close = document.getElementsByClassName("closebtn");
    var i;

    for (i = 0; i < close.length; i++) {
      close[i].onclick = function() {
        var div = this.parentElement;
        div.style.opacity = "0";
        setTimeout(function() {
          div.style.display = "none";
        }, 600);
      };
    }
  }
};
</script>

